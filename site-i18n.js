  function pick() {
    var s = null;
    try { s = localStorage.getItem('hakoniwa.site.language'); } catch (e) {}
    if (s && LANGS.some(function (l) { return l.code === s; })) return s;
    var nav = ((navigator.language || 'ja').slice(0, 2)).toLowerCase();
    return LANGS.some(function (l) { return l.code === nav; }) ? nav : 'ja';
  }

  // populate the select element
  function buildSelect() {
    var sel = document.getElementById('langSelect');
    if (!sel) return;
    LANGS.forEach(function (l) {
      var opt = document.createElement('option');
      opt.value = l.code;
      opt.textContent = l.name;
      sel.appendChild(opt);
    });
  }

  // 初回に各要素のインライン日本語(原文)を保存しておく。その場切替では原文が上書きされるため、
  // ja選択時・未訳キー時はこのスナップショットから復元する。
  var snapped = false;
  function snapshot() {
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      if (el.__ja === undefined) el.__ja = el.innerHTML;
    });
    snapped = true;
  }

  window.applySiteI18n = function (lang) {
    if (!snapped) snapshot();
    document.documentElement.lang = lang;
    var d = DICT[lang];
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var k = el.getAttribute('data-i18n');
      // ja or 辞書なし or 未訳キー → 保存した日本語原文へ復元
      var v = (lang === 'ja' || !d || d[k] === undefined) ? el.__ja : d[k];
      if (v === undefined || v === null) return;
      if (v.indexOf('<') !== -1) {
        el.innerHTML = v;
      } else {
        el.textContent = v;
      }
    });
    try { localStorage.setItem('hakoniwa.site.language', lang); } catch (e) {}
    var sel = document.getElementById('langSelect');
    if (sel) sel.value = lang;
  };

  document.addEventListener('DOMContentLoaded', function () {
    buildSelect();
    var sel = document.getElementById('langSelect');
    if (sel) {
      sel.addEventListener('change', function () {
        window.applySiteI18n(sel.value);
      });
    }
    window.applySiteI18n(pick());
  });
